package com.code4t2.carDealership.models;

public class SalespersonsModel {

	private int SalespersonID;
	private String SalespersonName;
	private int SalespersonMobile;
	private String SalespersonEmail;
	
	public int getSalespersonID() {
		return SalespersonID;
	}
	public void setSalespersonID(int salespersonID) {
		SalespersonID = salespersonID;
	}
	
	public String getSalespersonName() {
		return SalespersonName;
	}
	public void setSalespersonName(String salespersonName) {
		SalespersonName = salespersonName;
	}
	
	public int getSalespersonMobile() {
		return SalespersonMobile;
	}
	public void setSalespersonMobile(int salespersonMobile) {
		SalespersonMobile = salespersonMobile;
	}
	
	public String getSalespersonEmail() {
		return SalespersonEmail;
	}
	public void setSalespersonEmail(String salespersonEmail) {
		SalespersonEmail = salespersonEmail;
	}
	
}
